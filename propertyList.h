	GENICAMPROPGETBYNAME(CONTROLLER, decimationHorizontal); \
	GENICAMPROPGETBYNAME(CONTROLLER, decimationHorizontalMode); \
	GENICAMPROPGETBYNAME(CONTROLLER, decimationVertical); \
	GENICAMPROPGETBYNAME(CONTROLLER, decimationVerticalMode); \
	GENICAMPROPGETBYNAME(CONTROLLER, height); \
