#define SHOW_INTERESTED_PROPERTIES	1
#define SHOW_DEVICE_PROP			1
#define SHOW_IMG_FORMAT_PROP		1
#define SHOW_ACQUISION_PROP			1

#define GENICAMPROPGETBYNAME(CONTROLLER, NAME) \
do{ \
	std::cout << #NAME << " : " << CONTROLLER.NAME.readS() << std::endl; \
} while (0)

#define DUMPDEVICEPROP(CONTROLLER) \
{ \
	GENICAMPROPGETBYNAME(CONTROLLER, deviceModelName); \
	GENICAMPROPGETBYNAME(CONTROLLER, deviceFirmwareVersion); \
	GENICAMPROPGETBYNAME(CONTROLLER, deviceSerialNumber); \
	GENICAMPROPGETBYNAME(CONTROLLER, deviceTemperature); \
}


